<style>
.ant-input-number .ant-input__inner{
  text-align: left;
}
</style>

<template>
  <div class="app-container">

    <div class="filter-container">
      <a-form  layout="inline">
        <a-form-item label="机构搜索" >
          <a-input v-model:value="searchValue" @input="searchOffice" placeholder="机构名称、机构编码"></a-input>
        </a-form-item>
        <a-form-item >
          <a-button class="filter-item" type="primary" @click="searchOffice">
            <template #icon><ElIconSearchOutlined /></template>
            搜索
          </a-button>
          <a-button class="filter-item"  @click="() => { searchValue = ''; searchOffice() }">
            <template #icon><ElIconDeleteOutlined /></template>
            清空
          </a-button>
        </a-form-item>
      </a-form>
    </div>

    <a-row class="toolbar-container">
      <a-button class="filter-item" type="primary"  @click="addSubOffice('0')" v-permission="'office:save'">
        <template #icon><ElIconPlusOutlined /></template>
        添加机构
      </a-button>
      <a-button type="primary"  plain @click="expand">
       <template #icon><ElIconVerticalAlignMiddleOutlined /></template>
       展开/折叠
      </a-button>
    </a-row>

    <mb-table ref="table" v-bind="tableOptions" v-if="officeData && officeData.length > 0 && refreshTable" />

    <mb-dialog ref="officeFormDialog" width="700px" :title="dialogTitle" @confirm-click="save($event)">
      <template #content>
        <a-form ref="dataForm" :rules="rules" :model="temp" label-position="right" label-width="80px">
          <a-row :gutter="24">
            <a-col :span="12">
              <a-form-item label="机构类型" name="type">
                <mb-select v-model:value="temp.type" type="office_type" width="100%" :key="temp.type" />
              </a-form-item>
            </a-col>
            <a-col :span="12">
              <a-form-item label="上级机构" name="pid">
                <a-tree-select v-model:value="temp.pid" :key="temp.pid" :tree-data="officeTree" style="width: 100%" />
              </a-form-item>
            </a-col>
          </a-row>
          <a-row :gutter="24">
            <a-col :span="12">
              <a-form-item label="机构名称" name="name">
                <a-input v-model:value="temp.name" />
              </a-form-item>
            </a-col>
            <a-col :span="12">
              <a-form-item label="机构编码" name="code">
                <a-input v-model:value="temp.code" />
              </a-form-item>
            </a-col>
          </a-row>
          <a-row :gutter="24">
            <a-col :span="12">
              <a-form-item label="排序" name="sort">
                <a-input-number v-model:value="temp.sort" controls-position="right" />
              </a-form-item>
            </a-col>
          </a-row>
        </a-form>
      </template>
    </mb-dialog>

  </div>
</template>

<script setup>

import { ref, reactive, onMounted, watch, nextTick, getCurrentInstance } from 'vue'
const { proxy } = getCurrentInstance()
const refreshTable = ref(true)
const officeData = ref([])
const officeTree = ref([])
const searchValue = ref('')
const tableOptions = reactive({
  props: {
    'default-expand-all': true,
    'tree-props': { children: 'children', hasChildren: 'hasChildren' },
    'row-key': 'id'
  },
  showNo: false,
  page: false,
  cols: [
    {
      field: 'name',
      label: '机构名称',
      align: 'left',
      width: '15%',
      type: 'html'
    },
    {
      field: 'code',
      label: '机构编码',
      width: 300,
      align: 'left',
      type: 'html'
    },
    {
      field: 'type',
      dictType: 'office_type',
      label: '机构类型',
      width: 300,
      align: 'left'
    },
    {
      field: 'sort',
      label: '排序',
      width: 60
    },
    {
      label: '排序',
      type: 'btns',
      width: 150,
      btns: [
        {
          label: '上移',
          type: 'text',
          icon: 'ElIconUpOutlined',
          click: (row) => {
            proxy.$get('/system/office/sort/up',{
              id: row.id,
              pid: row.pid,
              sort: row.sort
            }).then(() => {
              reloadTable()
            })
          }
        },
        {
          label: '下移',
          type: 'text',
          icon: 'ElIconDownOutlined',
          click: (row) => {
            proxy.$get('/system/office/sort/down',{
              id: row.id,
              pid: row.pid,
              sort: row.sort
            }).then(() => {
              reloadTable()
            })
          }
        }
      ]
    },
    {
      label: '操作',
      type: 'btns',
      width: 310,
      fixed: 'right',
      align: 'left',
      btns: [
        {
          label: '添加下级机构',
          type: 'text',
          permission: 'office:save',
          icon: 'ElIconPlus',
          click: (row) => {
            addSubOffice(row.id)
          }
        },
        {
          label: '修改',
          type: 'text',
          permission: 'office:save',
          icon: 'ElIconEditOutlined',
          click: (row) => {
            handleUpdate(row)
          }
        },
        {
          label: '删除',
          type: 'text',
          permission: 'office:delete',
          icon: 'ElIconDeleteOutlined',
          if: (row) => {
            return row.pid != '0';
          },
          click: (row) => {
            proxy.$common.handleDelete({
              url: '/system/office/delete',
              id: row.id,
              done: () => reloadTable()
            })
          }
        },
        {
          permission: 'office:user:list',
          label: '用户列表',
          type: 'text',
          icon: 'ElIconUserFilled',
          click: (row) => {
            proxy.$router.push({
              path: '/system/user/user-list',
              query: { officeId: row.id }
            })
          }
        }
      ]
    }
  ]
})

const dialogTitle = ref('')
const temp = ref(getTemp())
const rules = reactive({
  type: [{ required: true, message: '请选择机构类型', trigger: 'blur' }],
  pid: [{ required: true, message: '请选择上级机构', trigger: 'blur' }],
  name: [{ required: true, message: '请输入机构名称', trigger: 'blur' }],
  code: [{ required: true, message: '请输入机构编码', trigger: 'blur' }]
})
var searchTimeout = null
const officeFormDialog = ref()
const dataForm = ref()

onMounted(() => {
  reloadTable()
})

watch(officeData, () => {
  officeTree.value = [{
    label: '根节点',
    value: '0',
    children: proxy.$treeTable.genTree(officeData.value)
  }]
})

function expand(){

  refreshTable.value = false;
  tableOptions.props["default-expand-allRows"] = !tableOptions.props["default-expand-allRows"];
  nextTick(() => refreshTable.value = true);

  // refreshTable.value = false
  // tableOptions.props["default-expand-all"] = !tableOptions.props["default-expand-all"]
  // nextTick(() => refreshTable.value = true)
}

function searchOffice() {
  clearTimeout(searchTimeout)
  searchTimeout = setTimeout(() => {
    if(searchValue.value){

      tableOptions.data = proxy.$treeTable.recursionSearch(['name', 'code'], proxy.$common.copyNew(officeData.value), searchValue.value)
    }else{
      tableOptions.data = officeData.value
    }
  },1000)
}

function getTemp() {
  return {
    id: '',
    name: '',
    sort: 0,
    pid: '',
    type: '',
    code: ''
  }
}

function resetTemp() {
  temp.value = getTemp()
}

function getSort() {
  proxy.$get('/system/office/sort', { pid: temp.value.pid }).then(res => {
    temp.value.sort = res.data
  })
}

function addSubOffice(id) {
  resetTemp()
  temp.value.pid = id
  temp.value.id = proxy.$common.uuid()
  getSort()
  dialogTitle.value = '添加'
  officeFormDialog.value.show()
  nextTick(() => {
    dataForm.value.clearValidate()
  })
}

function save(d) {
  dataForm.value.validate().then((valid) => {
    if (valid) {
      d.loading()
      if(temp.value.pid == temp.value.id){
        proxy.$notify({
          title: '失败',
          message: '上级机构不能选当前机构',
          type: 'error',
          duration: 2000
        })
        return
      }
      if(proxy.$treeTable.isChildren(proxy.$treeTable.queryChildren(officeData.value, temp.value.id), temp.value.pid)){
        proxy.$notify({
          title: '失败',
          message: '上级机构不能选当前机构子级',
          type: 'error',
          duration: 2000
        })
        return
      }
      proxy.$post('/system/office/save', temp.value).then(() => {
        d.hideLoading()
        proxy.$notify({
          title: '成功',
          message: dialogTitle.value + '成功',
          type: 'success',
          duration: 2000
        })
        reloadTable()
        officeFormDialog.value.hide()
      }).catch(() => d.hideLoading())
    }
  })
}

function reloadTable() {
  proxy.$get('/system/office/tree').then(res => {
    officeData.value = res.data.list
    tableOptions.data = officeData.value
  })
}

function handleUpdate(row) {
  for (var t in temp.value) {
    temp.value[t] = row[t]
  }
  temp.value.name = temp.value.name.replaceAll(/<font.*?>(.*?)<\/font>/g,'$1')
  dialogTitle.value = '修改'
  officeFormDialog.value.show()
  nextTick(() => {
    dataForm.value.clearValidate()
  })
}

</script>
<style scoped>
   .aForm{
    display: flex;
  }

  .aFormItem{
    margin-right: 30px;
  }
</style>